    private void initialize() {
        meetingServer=new MeetingServer();
        refresh();
        InetAddress addr;
        try {
            addr = InetAddress.getLocalHost();
            String ip=addr.getHostAddress().toString();//获得本机IP
            jLabel6.setText(ip);
        } catch (UnknownHostException ex) {
            Logger.getLogger(JServerFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextField1.setText(Setting.get("username"));
        jTextField2.setText(Setting.get("password"));
        
    }

    private void refresh() {
        new Thread(new Runnable(){

            @Override
            public void run() {
                while(true){
                    try {
                        jLabel3.setText(meetingServer.getMC().getMeetingState()+"");
                        jLabel4.setText(meetingServer.MC.getMeetingNo()+"");
                    } catch (RemoteException ex) {
                        Logger.getLogger(JServerFrame.class.getName()).log(Level.SEVERE, null, ex);
                    } 
                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(JServerFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
            
        }).start();
    }
